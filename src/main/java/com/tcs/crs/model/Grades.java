package com.tcs.crs.model;

public class Grades {
	
	private float grade;

	public Grades(float grade) {
		super();
		this.grade = grade;
	}
	
	public Grades()
	{
		
	}

	/**
	 * @return the grade
	 */
	public float getGrade() {
		return grade;
	}

	/**
	 * @param grade the grade to set
	 */
	public void setGrade(float grade) {
		this.grade = grade;
	}
	
	

}
